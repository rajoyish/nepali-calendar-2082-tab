const replaceSymbols = (
  textToConvert,
  symbolCount,
  symbolsToReplace,
  correspondingSymbols,
  isToUnicode,
) => {
  if (textToConvert === "") return textToConvert;

  for (let symbolIdx = 0; symbolIdx < symbolCount; symbolIdx++) {
    let index = textToConvert.indexOf(symbolsToReplace[symbolIdx]);
    while (index !== -1) {
      textToConvert = textToConvert.replace(
        symbolsToReplace[symbolIdx],
        correspondingSymbols[symbolIdx],
      );
      index = textToConvert.indexOf(symbolsToReplace[symbolIdx], index + 1);
    }
  }

  if (isToUnicode) {
    let positionOfI = textToConvert.indexOf("l");
    while (positionOfI !== -1) {
      let nextChar = textToConvert.charAt(positionOfI + 1);
      textToConvert = textToConvert.replace("l" + nextChar, nextChar + "а§њ");
      positionOfI = textToConvert.search(/l/, positionOfI + 1);
    }

    let positionOfWrongEe = textToConvert.indexOf("а§ња•Н");
    while (positionOfWrongEe !== -1) {
      let consonantNextToWrongEe = textToConvert.charAt(positionOfWrongEe + 2);
      textToConvert = textToConvert.replace(
        "а§ња•Н" + consonantNextToWrongEe,
        "а•Н" + consonantNextToWrongEe + "а§њ",
      );
      positionOfWrongEe = textToConvert.search(/а§ња•Н/, positionOfWrongEe + 2);
    }

    const setOfMatras = "а§Њ а§њ а•А а•Б а•В а•Г а•З а•И а•Л а•М а§В : а§Б а•Е";
    let positionOfReph = textToConvert.indexOf("{");
    while (positionOfReph > 0) {
      let probablePositionOfHalfR = positionOfReph - 1;
      let charAtProbablePositionOfHalfR = textToConvert.charAt(
        probablePositionOfHalfR,
      );

      while (setOfMatras.includes(charAtProbablePositionOfHalfR)) {
        probablePositionOfHalfR--;
        charAtProbablePositionOfHalfR = textToConvert.charAt(
          probablePositionOfHalfR,
        );
      }

      let charToReplace = textToConvert.substr(
        probablePositionOfHalfR,
        positionOfReph - probablePositionOfHalfR,
      );
      textToConvert = textToConvert.replace(
        charToReplace + "{",
        "а§∞а•Н" + charToReplace,
      );
      positionOfReph = textToConvert.indexOf("{");
    }

    textToConvert = textToConvert
      .replace(/=/g, ".")
      .replace(/_/g, ")")
      .replace(/√Ц/g, "=")
      .replace(/√Щ/g, ";")
      .replace(/вА¶/g, "вАШ")
      .replace(/√Ъ/g, "вАЩ")
      .replace(/√Ы/g, "!")
      .replace(/√Ь/g, "%")
      .replace(/√¶/g, "вАЬ")
      .replace(/√Ж/g, "вАЭ")
      .replace(/¬±/g, "+")
      .replace(/-/g, "(")
      .replace(/</g, "?");
  } else {
    let positionOfI = textToConvert.indexOf("а§њ");
    while (positionOfI !== -1) {
      let prevChar = textToConvert.charAt(positionOfI - 1);
      textToConvert = textToConvert.replace(prevChar + "а§њ", "l" + prevChar);
      positionOfI = textToConvert.search(/а§њ/, positionOfI + 1);
    }
  }

  return textToConvert;
};

export async function transliterateNepali(text, maxSuggestions = 8) {
  if (!text || text.trim() === "") return [];

  const params = new URLSearchParams({
    text: text,
    itc: "ne-t-i0-und",
    num: String(maxSuggestions),
    cp: "0",
    cs: "1",
    ie: "utf-8",
    oe: "utf-8",
    app: "demopage",
  });

  try {
    const response = await fetch(
      `https://inputtools.google.com/request?${params.toString()}`,
      {
        method: "POST",
        headers: { "Content-Type": "application/x-www-form-urlencoded" },
      },
    );

    if (!response.ok) return [text];
    const data = await response.json();
    if (data[0] === "SUCCESS" && Array.isArray(data[1]?.[0]?.[1])) {
      return data[1][0][1];
    }
    return [text];
  } catch (error) {
    return [text];
  }
}
